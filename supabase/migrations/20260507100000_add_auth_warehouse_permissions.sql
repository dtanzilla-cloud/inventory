create or replace view public.inventory_summary
with (security_invoker = true)
as
select
  product,
  warehouse,
  coalesce(sum(pieces) filter (where activity_date < current_date), 0) as in_qty,
  coalesce(abs(sum(pieces) filter (where activity_date >= current_date and pieces < 0)), 0) as reserved_qty,
  coalesce(sum(pieces) filter (where activity_date >= current_date and pieces > 0), 0) as incoming_qty
from public.activities
group by product, warehouse;

grant select on public.inventory_summary to authenticated;

