create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text not null,
  role text not null check (role in ('owner', 'warehouse')),
  warehouse text,
  check (
    (role = 'owner' and warehouse is null)
    or (role = 'warehouse' and warehouse is not null)
  )
);

create table if not exists public.documents (
  id bigint generated by default as identity primary key,
  activity_id bigint not null references public.activities(id) on delete cascade,
  file_name text not null,
  file_url text not null,
  created_at timestamptz not null default now()
);

create table if not exists public.charges (
  id bigint generated by default as identity primary key,
  month text not null,
  warehouse text,
  payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

insert into storage.buckets (id, name, public)
values ('activity-documents', 'activity-documents', true)
on conflict (id) do nothing;

create or replace function public.current_profile_role()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select role from public.profiles where id = auth.uid()
$$;

create or replace function public.current_profile_warehouse()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select warehouse from public.profiles where id = auth.uid()
$$;

create or replace function public.can_access_warehouse(target_warehouse text)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.current_profile_role() = 'owner'
    or (
      public.current_profile_role() = 'warehouse'
      and public.current_profile_warehouse() = target_warehouse
    )
$$;

alter table public.profiles enable row level security;
alter table public.activities enable row level security;
alter table public.documents enable row level security;
alter table public.charges enable row level security;

do $$
declare
  policy_record record;
begin
  for policy_record in
    select n.nspname, c.relname, p.polname
    from pg_policy p
    join pg_class c on c.oid = p.polrelid
    join pg_namespace n on n.oid = c.relnamespace
    where n.nspname = 'public'
      and c.relname in ('activities', 'documents', 'charges')
      and p.polcmd in ('a', 'd', '*')
      and (p.polroles = '{0}'::oid[] or 'public'::regrole::oid = any(p.polroles))
  loop
    execute format('drop policy if exists %I on %I.%I', policy_record.polname, policy_record.nspname, policy_record.relname);
  end loop;

  for policy_record in
    select n.nspname, c.relname, p.polname
    from pg_policy p
    join pg_class c on c.oid = p.polrelid
    join pg_namespace n on n.oid = c.relnamespace
    where n.nspname = 'storage'
      and c.relname = 'objects'
      and p.polcmd in ('a', 'd', '*')
      and (p.polroles = '{0}'::oid[] or 'public'::regrole::oid = any(p.polroles))
  loop
    execute format('drop policy if exists %I on %I.%I', policy_record.polname, policy_record.nspname, policy_record.relname);
  end loop;
end $$;

